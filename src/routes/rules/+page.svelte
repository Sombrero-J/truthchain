<div class="bigBox">
  <h1>Guidelines</h1>
  <h2>For Authors</h2>
  <div class="subtitle">Title</div>
  <div class="body">
    Ensure the title is clear, relevant to the article’s subject matter and
    adopts a neutral tone to maintain objectivity. Title must be concise
    (15-word limit).
  </div>
  <div class="subtitle">Thumbnail</div>
  <div class="body">
    Please select an image directly relevant to your content. It should be
    clear, high-quality and not misleading.
  </div>
  <div class="subtitle">Content Body</div>
  <div class="body">
    <ul>
      <li class="body">
        Write with clarity, focusing on factual accuracy and a neutral tone to
        avoid bias. Avoid personal attacks or derogatory language.
      </li>
      <li class="body">
        Content must be original and not a paraphrase of another author’s
        content.
      </li>
      <li class="body">
        Do not share or encourage the sharing of sexual, abusive or suggestive
        content involving minors. Any predatory or inappropriate behavior
        involving a minor is also strictly prohibited.
      </li>
    </ul>
  </div>
  <div class="subtitle">Proof of stake</div>
  <ul>
    <li class="body">
      Understand that your proof of stake is a commitment to the truthfulness of
      your content.
    </li>
    <li class="body">
      Be prepared to lose this stake if your content is deemed inaccurate after
      verification.
    </li>
  </ul>
  <div class="subtitle">Anonymous credibility</div>
  <ul>
    <li class="body">
      Build credibility through the consistent quality and reliability of your
      content.
    </li>
    <li class="body">
      Your content’s value and the trust it garners from the community become
      your identity.
    </li>
  </ul>
  <div class="subtitle">Upholding integrity</div>
  <ul>
    <li class="body">
      Despite anonymity, maintain a high level of integrity in creating your
      content. Anonymity does not diminish the responsibility to produce
      accurate, fair, and respectful content.
    </li>
    <li class="body">
      Leverage anonymity to express honest opinions without fear but avoid using
      anonymity for malicious behaviours or unfounded accusations.
    </li>
  </ul>
  <div class="subtitle">Security and privacy</div>
  <ul>
    <li class="body">
      When citing sources or evidence, ensure you do not mistakenly reveal your
      identity or that of others who wish to remain anonymous. This includes
      avoiding personal anecdotes or specifics that could lead to
      identification.
    </li>
    <li class="body">
      Do not attempt to deduce or reveal the identities of other users. The
      strength of the platform focuses on content, not the personal details of
      its contributors.
    </li>
  </ul>
</div>

